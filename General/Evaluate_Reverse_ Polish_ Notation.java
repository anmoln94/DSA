/*  ["4", "13", "5", "/", "+"] evaluated as (4 + (13 / 5)) so return 6.
 *  Use stack. As soon as u encounter an operator, pop recent two elements , evaluate them and push back the result.
 */
public int evalRPN(String[] tokens) 
 {
        int a,b;
		Stack<Integer> S = new Stack<Integer>();
		for (String s : tokens) {
			if(s.equals("+")) {
				S.add(S.pop()+S.pop());
			}
			else if(s.equals("/")) {
				b = S.pop();
				a = S.pop();
				S.add(a / b);
			}
			else if(s.equals("*")) {
				S.add(S.pop() * S.pop());
			}
			else if(s.equals("-")) {
				b = S.pop();
				a = S.pop();
				S.add(a - b);
			}
			else {
				S.add(Integer.parseInt(s));
			}
		}	
		return S.pop();
}